const normalize = (str) => {
  if (!str) return "";
  return str.toLowerCase().replace(/[^a-z0-9]/g, "").trim();
};

const findCountry = (input) => {
  const normalizedInput = normalize(input);
  return countriesData.find(country => {
    const nameMatch = normalize(country.name) === normalizedInput;
    const aliasMatch = country.aliases.some(alias => normalize(alias) === normalizedInput);
    return nameMatch || aliasMatch;
  });
};

